#   VARIÁVEIS
#   -----------------------------------------------------------
#   * Todas as variáveis são objetos
#
#   * Não precisam ser declaradas
#
#   * Não precisamos dizer que tipo de objeto 
#   é determinada variavel para poder utilizar
#
#   * São criadas no momento em que lhe atribuímos valor
#   
#   * Começa a partir de uma letra obrigatoriamente
#
#   * Apenas  podem conter caracteres, algarismos e underscores
#
#   * nomes são case-sensitive